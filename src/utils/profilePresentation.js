export function buildAvatarInitials(name) {
  return name
    .trim()
    .split(/\s+/)
    .slice(0, 2)
    .map((item) => item[0]?.toUpperCase() || '')
    .join('');
}

export function getFactorProfileExplanation(factor, rawScore) {
  const score = getDisplayFactorScore(factor.key, rawScore);
  const bucket = score >= 6.5 ? 'high' : score <= 3.5 ? 'low' : 'mid';

  return FACTOR_EXPLANATIONS[factor.key]?.[bucket] || '';
}
